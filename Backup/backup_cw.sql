--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE FUNCTION "INF2S_FALKOWSKIE"."WYNAGRODZENIE" (id Employees.employee_id%type)
RETURN NUMBER
IS
    roczne NUMBER;
    salary NUMBER;
    commission_pct NUMBER;
BEGIN
    SELECT salary, commission_pct 
    INTO salary, commission_pct
    FROM Employees
    WHERE employee_id = id;

    IF commission_pct IS NULL THEN
        commission_pct := 0;
    END IF;

    roczne := salary * 12 + (salary * commission_pct);

    RETURN roczne;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 0;
    WHEN OTHERS THEN
        RETURN NULL;
END;

